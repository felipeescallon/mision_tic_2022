public class Persona {
    private String Cedula;
    private int Edad;
    private String Nombre;

    public Persona(String cedula, int edad, String nombre) {
        Cedula = cedula;
        Edad = edad;
        Nombre = nombre;
    }

    public String getCedula() {
        return Cedula;
    }

    public int getEdad() {
        return Edad;
    }

    public String getNombre() {
        return Nombre;
    }
    
    
    
    
    
    
}
